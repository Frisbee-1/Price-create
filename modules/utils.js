  if (!name || typeof name !== "string") return ""; // –∑–∞—â–∏—Ç–∞ –æ—Ç undefined/null

  return name
    .replace(/\s+/g, " ")   // –∑–∞–º–µ–Ω—è–µ—Ç –Ω–µ—Å–∫–æ–ª—å–∫–æ –ø—Ä–æ–±–µ–ª–æ–≤ –æ–¥–Ω–∏–º
